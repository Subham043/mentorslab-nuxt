    name: "ParentingSkillPage",
    components: { FrontendClientCardComponent, SliderNavButtonComponent, FrontendTestimonialCardComponent },
    layout: "FrontendLayout",
    data() {
        return {
          activeName: '1',
          dialogFormVisible: false,
          name: "",
          email: "",
          phone: "",
          message: "",
          file: null,
          slickOptions: {
              arrows: false,
              dots: false,
              infinite: true,
              autoplay: true,
              autoplaySpeed: 5000,
              draggable: true,
              pauseOnHover: true,
              swipe: true,
              slidesToShow: 3,
              slidesToScroll: 1,
              responsive: [
                  {
                      breakpoint: 1024,
                      settings: {
                          slidesToShow: 1,
                          slidesToScroll: 1,
                          infinite: true,
                          dots: false,
                      },
                  },
                  {
                      breakpoint: 600,
                      settings: {
                          slidesToShow: 1,
                          slidesToScroll: 1,
                          dots: false,
                      },
                  },
              ],
          },
        };
    },
    head: {
        script: [
            { src: 'https://checkout.razorpay.com/v1/checkout.js' },
        ],
    },
    mounted() {
        // eslint-disable-next-line nuxt/no-env-in-hooks
        if (process.client) {
            this.$scrollTo("#__nuxt", 0, { force: true });
        }
    },
    methods: {
      nextTestimonialNavClick(){
          this.$refs.slickTestimonial.next()
      },
      prevTestimonialNavClick(){
          this.$refs.slickTestimonial.prev()
      },
      async formHandler() {
        const loading = this.$loading({
        lock: true,
        fullscreen: true,
        });
        try {
            const formData = new FormData;
            formData.append('name', this.name);
            formData.append('email', this.email);
            formData.append('phone', this.phone);
            formData.append('message', !!this.message);
            if(this.file){
              formData.append('cv', this.file);
            }
            await this.$publicApi.post('/subscription-user/validate/',formData); // eslint-disable-line
            const response = await this.$publicApi.get('/subscription-user/generate-payment-order'); // eslint-disable-line
            this.loadRazorpay(response.data.data)
            // console.log(response);// eslint-disable-line
        } catch (err) {
            // console.log(err.response);// eslint-disable-line
            this.$refs.form.setErrors({
              email: err?.response?.data?.form_error?.email,
              message: err?.response?.data?.form_error?.message,
              name: err?.response?.data?.form_error?.name,
              phone: err?.response?.data?.form_error?.phone,
              cv: err?.response?.data?.form_error?.cv,
            });
            if(err?.response?.data?.message) this.$toast.error(err?.response?.data?.message)
            if(err?.response?.data?.error) this.$toast.error(err?.response?.data?.error)

        }finally{
            loading.close()
        }
      },
      loadRazorpay(data){
        const options = {
                key: this.$config.RAZORPAY_KEY_ID,
                amount: data.amount,
                currency: data.currency,
                description: "Payment description",
                order_id: data.id,
                prefill: {
                name: this.name,
                email: this.email,
                contact: this.phone
            },
            theme: {
                color: "#000000" // Set your website theme color
            },
            handler: async (response) => {
                await this.verifyPayment(response, data)
            }
        };

        // eslint-disable-next-line no-undef
        const rzp = new Razorpay(options);
        rzp.open();
      },
      async verifyPayment(data, order){
          const loading = this.$loading({
              lock: true,
              fullscreen: true,
          });
          try {
              const formData = new FormData;
              formData.append('name', this.name);
              formData.append('email', this.email);
              formData.append('phone', this.phone);
              formData.append('message', this.message);
              formData.append('amount', order.amount);
              formData.append('receipt', order.receipt);
              formData.append('razorpayOrderId', data.razorpay_order_id);
              formData.append('razorpayPaymentId', data.razorpay_payment_id);
              formData.append('signature', data.razorpay_signature);
              if(this.file){
                formData.append('cv', this.file);
              }
              const response = await this.$publicApi.post('/subscription-user/verify-payment',formData); // eslint-disable-line
              this.$toast.success(response.data.data.message)
              if(this.$auth.loggedIn){
                await this.$auth.logout();
              }
              await this.$auth.setUser({
                id: response.data.data.order.userId,
                name: response.data.data.order.name,
                email: response.data.data.order.email,
                phone: response.data.data.order.phone,
                role: response.data.data.order.role,
                blocked: response.data.data.order.blocked,
                verified: response.data.data.order.verified,
                createdAt: response.data.data.order.createdAt,
                updatedAt: response.data.data.order.updatedAt,
              })
              await this.$auth.setUserToken(response.data.data.order.access_token, response.data.data.order.refresh_token)
              this.name=''
              this.email=''
              this.phone=''
              this.message=''
              this.file = null
              this.$refs.form.reset()
              this.dialogFormVisible = false;
          } catch (err) {
              this.$refs.form.setErrors({
                  email: err?.response?.data?.form_error?.email,
                  message: err?.response?.data?.form_error?.message,
                  name: err?.response?.data?.form_error?.name,
                  phone: err?.response?.data?.form_error?.phone,
                  cv: err?.response?.data?.form_error?.cv,
              });
              if(err?.response?.data?.message) this.$toast.error(err?.response?.data?.message)
              if(err?.response?.data?.error) this.$toast.error(err?.response?.data?.error)
          } finally{
              loading.close()
